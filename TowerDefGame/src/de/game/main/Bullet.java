package de.game.main;

public class Bullet {
	private int damage;
	private int vel;
	private int f_x;
	private int f_y;
	private int ang;
	private int x;
	private int y;
	
	
	public int getDamage() {
		return damage;
	}
	public void setDamage(int damage) {
		this.damage = damage;
	}
	public int getVel() {
		return vel;
	}
	public void setVel(int vel) {
		this.vel = vel;
	}
	public int getF_x() {
		return f_x;
	}
	public void setF_x(int f_x) {
		this.f_x = f_x;
	}
	public int getF_y() {
		return f_y;
	}
	public void setF_y(int f_y) {
		this.f_y = f_y;
	}
	public int getAng() {
		return ang;
	}
	public void setAng(int ang) {
		this.ang = ang;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
}
